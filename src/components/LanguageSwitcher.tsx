import { useGuest, Language } from '@/contexts/GuestContext';
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuTrigger,
} from '@/components/ui/dropdown-menu';
import { Button } from '@/components/ui/button';

const languages: { code: Language; flag: string; name: string }[] = [
  { code: 'da', flag: 'ðŸ‡©ðŸ‡°', name: 'Dansk' },
  { code: 'en', flag: 'ðŸ‡¬ðŸ‡§', name: 'English' },
  { code: 'de', flag: 'ðŸ‡©ðŸ‡ª', name: 'Deutsch' },
  { code: 'nl', flag: 'ðŸ‡³ðŸ‡±', name: 'Nederlands' },
];

export const LanguageSwitcher = () => {
  const { language, setLanguage } = useGuest();
  const currentLang = languages.find(l => l.code === language);

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button variant="ghost" size="sm" className="text-lg px-2">
          {currentLang?.flag}
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end">
        {languages.map((lang) => (
          <DropdownMenuItem
            key={lang.code}
            onClick={() => setLanguage(lang.code)}
            className={language === lang.code ? 'bg-accent' : ''}
          >
            <span className="mr-2 text-lg">{lang.flag}</span>
            {lang.name}
          </DropdownMenuItem>
        ))}
      </DropdownMenuContent>
    </DropdownMenu>
  );
};
